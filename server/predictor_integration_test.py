    def setUp(self):
        # Load the trained network from the JSON file
        self.network = Network.init_from_model(TRAINED_MODEL_FILE)

        # Create a dummy input image (flattened 28x28 MNIST image with random values between 0 and 1)
        # You can replace this with a real image input for a more comprehensive test.
        self.test_image = [0.5] * 784  # All pixel values set to 0.5 for simplicity


    def test_predict(self):
        # Call the predict function
        prediction = predict(self.network, self.test_image)

        # Check if the prediction is within valid bounds (since it's an MNIST-like problem, it should predict 0-9)
        self.assertGreaterEqual(prediction, 0)
        self.assertLessEqual(prediction, 9)


    def test_predict_number_1(self):
        predicted_value = predict(self.network, self.number_1_image)
        self.assertEqual(predicted_value, 1, "The predicted value should be 1.")


    def test_model_consistency(self):
        # Run the prediction multiple times and ensure consistency (network should produce the same result)
        first_prediction = predict(self.network, self.test_image)
        for _ in range(5):
            self.assertEqual(predict(self.network, self.test_image), first_prediction)


